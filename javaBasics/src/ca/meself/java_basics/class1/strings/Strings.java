package ca.meself.java_basics.class1.strings;

public class Strings {

	public static void main(String[] args) {
		System.out.println("Hello from the Strings class!");
		
		int myInt = 16;
		
		String text = "my journey has not been a good one.";
		String blank = " ";
		String name = "Name";
		
		System.out.println("Hi, I'm" + blank + name + " and sadly, " + text);
		System.out.println(myInt + " is just a number");
	}
}
